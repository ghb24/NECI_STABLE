               allocate(ZIA(2*(NMSH+1)*NMAX*NMAX))
               LogAlloc(ierr,'ZIA',2*(NMSH+1)*NMAX*NMAX,16,tagZIA)
               WRITE(6,*) NMSH,NMAX
    !!C..
